function formatDate(value) {
    if (!value) return '';
    return new Date(value).toLocaleString('en-US', {
        weekday: 'long',
        year: 'numeric',
        month: 'long',
        day: 'numeric',
        hour: '2-digit',
        minute: '2-digit',
        second: '2-digit'
    });
}

module.exports = { formatDate };